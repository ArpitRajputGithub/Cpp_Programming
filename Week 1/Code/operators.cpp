/*Operators are used to perform operations on variables and values.

C++ divides the operators into the following groups:

Arithmetic operators --> +, -, *, /, %, a++, a--
Assignment operators
Comparison operators
Logical operators
Bitwise operators




*/

#include <iostream>
using namespace std;
int main()
{
     int x = 4;
     x += 5;
     cout << x << endl;
     x -= 2;
     cout << x << endl;
     x *= 5;
     cout << x << endl;
     x /= 3;
     cout << x;
}